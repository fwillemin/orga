            <h2>Les logiciels installés sur votre ordinateur</h2>

            Le plus gros avantage de ces logiciels est de ne pas avior besoin d'internet pour fonctionner.
            <br>Selon l'importance du logiciel, il peut nécéssité la présence d'un serveur dans votre entreprise avec tout ce que cela implique : achat de matériel informatique, frais d'installation, de maintenance et nécésité de mettre à jour les versions pour rester compatible avec vos ordinateurs. Ces mises à jour état bien souvent payantes.
            <br>L'autre aspect à prendre en compte est l'obligation de faire des sauvegardes régulières et de les garder en dehors de votre entreprise. Fastidieux et coûteux une nouvelle fois.
            <br>
            <br>
            <h2>Les logiciels en ligne</h2>
            Le frein au développement de ces logiciels était la stabilité et la rapidité d'internet. C'est une époque révolue. Aujourd'hui, 99% des entreprises ont un accès internet et les coupures sont devenues rarissimes.
            Alors, tous les éditeurs de logiciels de nouvelle génération propose désormais leur logiciels en ligne car les avantages sont considérables.
            <ul>
                <li>Plus besoin d'installation. Ils fontionnent dans votre navigateur Intenret</li>
                <li>Vos données sont accéssibles partout sur n'importe quel ordinateur</li>
                <li>Plus besoin de gérer des versions de fichiers type tableur ou autre</li>
                <li>Les sauvegardes sont faites pour vous automatiquement</li>
                <li>Aucun frais pour votre matériel informatique, la maintenance, les mises à jour, ...</li>
                <li>Le logiciel évolue sans que vous n'ayez quoi que se soit à penser, vous avez toujours la dernière version</li>
                <li>...</li>
            </ul>
            Alors, tout cela implique que vos données soient stockées sur les serveurs du logiciel et plus chez vous. A moins de stocker des données privées sensibles (médicales, bancaires, ...) cette situation n'est que bénéfique tant vos données sont bien plus en sécurité là-bas que chez vous (défaillance, vol, incendie, ...).
            <br><br><h4>Conclusion</h4>
            les énormes avantages des logiciels en ligne combinés à l'augmentation du débit internet global font que dans les années à venir, les logiciels installés en local sur vos ordianteurs vont disparaître.
            <br>C'est pour votre entreprise une économie financière certaine et surtout, cela sécurise vos donnéess en les rendant indépendantes de votre matériel informatique et des dangers auxquels il est exposé.
